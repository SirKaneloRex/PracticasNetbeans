    void dibujarPunto(Point p, Color color){
        Graphics2D g2d = (Graphics2D) this.jPanel3.getGraphics();
        
        g2d.setColor(color);
        g2d.drawLine(p.x,p.y,p.x,p.y);
    }

    
    private void agregarLinea(Linea linea) {
        this.aFiguras.add(linea);
        Graphics2D g2d = (Graphics2D) this.jPanel3.getGraphics();
        linea.dibujar(g2d);
    }
    
    private void agregarTriangulo(Point punto1, Point punto2, Point punto3, Color color) {
        Linea l1, l2, l3;
        l1 = new Linea(punto1,punto2,color);
        l2 = new Linea(punto2,punto3,color);
        l3 = new Linea(punto3,punto1,color);
        
        Triangulo triangulo = new Triangulo(l1,l2,l3,color);
                
        this.aFiguras.add(triangulo);
        
        Graphics2D g2d = (Graphics2D) this.jPanel3.getGraphics();
        
        triangulo.dibujar(g2d);
        
        System.out.println("Se agrego triangulo");
        
    }
    
