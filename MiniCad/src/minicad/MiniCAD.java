    private void jPanel3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel3MouseClicked
        // TODO add your handling code here:
        int x = evt.getX();
        int y = evt.getY();
        
        switch (figura){
            case LINEA:
                if (estado == this.ESPERANDO_PRIMER_PUNTO){
                    this.punto1.x = x;
                    this.punto1.y = y;
                    punto2.x = punto1.x+1;
                    punto2.y = punto1.y+1;
                    this.dibujarPunto(punto1,this.color);

                    estado = this.ESPERANDO_SEGUNDO_PUNTO;
                } else {
                    this.punto2.x = x;
                    this.punto2.y = y;
                    agregarLinea(new Linea(this.punto1, this.punto2,this.color));
                    estado = this.ESPERANDO_PRIMER_PUNTO;
                }
                break;
                
            case TRIANGULO:
                if (estado == this.ESPERANDO_PRIMER_PUNTO){
                    this.punto1.x = x;
                    this.punto1.y = y;
                    punto2.x = punto1.x+1;
                    punto2.y = punto1.y+1;
                    System.out.println("Se agrego primer punto");
                    estado = this.ESPERANDO_SEGUNDO_PUNTO;
                } else if (estado == this.ESPERANDO_SEGUNDO_PUNTO){
                    this.punto2.x = x;
                    this.punto2.y = y;
                    System.out.println("Se agrego segundo punto");
                    estado = this.ESPERANDO_TERCER_PUNTO;                    
                    
                } else if (estado == this.ESPERANDO_TERCER_PUNTO) {
                    this.punto3.x = x;
                    this.punto3.y = y;
                    agregarTriangulo(this.punto1, this.punto2,punto3,this.color);
                    estado = this.ESPERANDO_PRIMER_PUNTO;
                    System.out.println("Se agrego tercer punto");
                }
                break;                                
            case CUADRADO:
                if (estado == this.ESPERANDO_PRIMER_PUNTO){
                    this.punto1.x = x;
                    this.punto1.y = y;
                    punto2.x = punto1.x+1;
                    punto2.y = punto1.y+1;
                    this.dibujarPunto(punto1,this.color);

                    estado = this.ESPERANDO_SEGUNDO_PUNTO;
                } else {
                    this.punto2.x = x;
                    this.punto2.y = y;
                    agregarCuadrado(new Cuadrado(this.punto1,this.punto2,this.color));
                    estado = this.ESPERANDO_PRIMER_PUNTO;
                }
                break;
            case CIRCULO:
                if (estado == this.ESPERANDO_PRIMER_PUNTO){                    
                    this.punto1.x = x;
                    this.punto1.y = y;
                    punto2.x = punto1.x+1;
                    punto2.y = punto1.y+1;
                    System.out.println("Se dibuja punto");

                    estado = this.ESPERANDO_SEGUNDO_PUNTO;
                } else {
                    this.punto2.x = x;
                    this.punto2.y = y;
                    System.out.println("Se agrega el circulo");
                    agregarCirculo(new Circulo(this.punto1, Math.abs(this.punto2.x-this.punto1.x),this.color));
                    estado = this.ESPERANDO_PRIMER_PUNTO;
                }
                break;
            case OVALO:
                if (estado == this.ESPERANDO_PRIMER_PUNTO){                    
                    this.punto1.x = x;
                    this.punto1.y = y;
                    punto2.x = punto1.x+1;
                    punto2.y = punto1.y+1;
                    System.out.println("Se dibuja punto");

                    estado = this.ESPERANDO_SEGUNDO_PUNTO;
                } else {
                    this.punto2.x = x;
                    this.punto2.y = y;
                    System.out.println("Se agrega el circulo");
                    agregarOvalo(new Ovalo(this.punto1, this.punto2,this.color));
                    estado = this.ESPERANDO_PRIMER_PUNTO;
                }
                break;
            case TEXTO:
                if (estado == this.ESPERANDO_PRIMER_PUNTO){                    
                    this.punto1.x = x;
                    this.punto1.y = y;
                    punto2.x = punto1.x+1;
                    punto2.y = punto1.y+1;
                    System.out.println("Se dibuja punto");
                    
                    Graphics2D g2d = (Graphics2D) this.jPanel3.getGraphics();
                    
                    g2d.drawString(this.jTextField1.getText(), this.punto1.x, this.punto1.y);

                    estado = this.ESPERANDO_PRIMER_PUNTO;
                }
                break;                 
        }        
    }//GEN-LAST:event_jPanel3MouseClicked

