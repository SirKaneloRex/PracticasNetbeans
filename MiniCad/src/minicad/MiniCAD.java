    void redibujar(){
        Graphics2D g2d = (Graphics2D) this.jPanel3.getGraphics();
        
        Iterator itr = aFiguras.iterator();
        int i = 0;
        while(itr.hasNext()){
            Figura f = (Figura)itr.next();
            
            if (f instanceof Linea){
                Linea l = (Linea)f;
                l.dibujar(g2d);
            }
            
            if (f instanceof Triangulo){
                Triangulo t = (Triangulo)f;
                t.dibujar(g2d);
            }                       
        }   
    }
    
